function rnd(n) {
  const x = Math.sin(n * 127.1 + 311.7) * 43758.5453
  return x - Math.floor(x)
}

export default function MarqueeTitle({
  text = 'Torneo 2do|Aniversario',
  variant = 'warm',
  color,
  scale = 1,
  label,
}) {
  const lines = String(text).toUpperCase().split(/[|\n]/)
  const rootStyle = {}
  if (color) rootStyle['--on'] = color
  if (scale && scale !== 1) {
    rootStyle.transform = `scale(${scale})`
    rootStyle.transformOrigin = 'center'
  }

  return (
    <div
      className={`mq-root v-${variant}`}
      style={rootStyle}
      role="img"
      aria-label={label || String(text).replace(/[|\n]/g, ' ')}
    >
      {lines.map((line, li) => (
        <div className="mq-line" key={li}>
          {[...line].map((ch, ci) => {
            const glyph = FONT[ch] || FONT['?']
            return (
              <span className="mq-char" key={ci} aria-hidden="true">
                {glyph.map((row, r) =>
                  [...row].map((bit, c) => {
                    const on = bit === '1'
                    if (!on) {
                      return <span key={`${r}-${c}`} className="mq-bulb off" />
                    }
                    const seed = (li * 113 + ci) * 41 + r * 7 + c
                    const dur = (3.4 + rnd(seed) * 4.0).toFixed(2)
                    const delay = (-rnd(seed + 17) * 7).toFixed(2)
                    return (
                      <span
                        key={`${r}-${c}`}
                        className="mq-bulb on"
                        style={{ animationDuration: `${dur}s`, animationDelay: `${delay}s` }}
                      />
                    )
                  })
                )}
              </span>
            )
          })}
        </div>
      ))}
    </div>
  )
}
